export type LanguageCode = 'es' | 'en'
export type Language = typeof es
export type TranslationKey = string

// Current language (default to Spanish)
let currentLanguage: Language = es

export const setLanguage = (lang: LanguageCode) => {
  currentLanguage = lang === 'es' ? es : en
}

export const getCurrentLanguageCode = (): LanguageCode => {
  return currentLanguage === es ? 'es' : 'en'
}

// Helper function to get nested translation
export const t = (key: string): string => {
  const keys = key.split('.')
  let value: any = currentLanguage

  for (const k of keys) {
    if (value && typeof value === 'object' && k in value) {
      value = value[k]
    } else {
      return key // Return the key itself if not found
    }
  }

  return typeof value === 'string' ? value : key
}

