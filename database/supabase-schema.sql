ALTER TABLE appointments ENABLE ROW LEVEL SECURITY;
ALTER TABLE payments ENABLE ROW LEVEL SECURITY;
ALTER TABLE reviews ENABLE ROW LEVEL SECURITY;
ALTER TABLE notifications ENABLE ROW LEVEL SECURITY;
ALTER TABLE chat_rooms ENABLE ROW LEVEL SECURITY;
ALTER TABLE chat_messages ENABLE ROW LEVEL SECURITY;
ALTER TABLE therapist_services ENABLE ROW LEVEL SECURITY;
ALTER TABLE therapist_availability ENABLE ROW LEVEL SECURITY;

-- Profiles policies
CREATE POLICY "Users can view their own profile" ON profiles FOR SELECT USING (auth.uid() = id);
CREATE POLICY "Users can update their own profile" ON profiles FOR UPDATE USING (auth.uid() = id);
CREATE POLICY "Admins can view all profiles" ON profiles FOR SELECT USING (auth.jwt()->>'role' = 'ADMIN');
CREATE POLICY "Admins can insert profiles" ON profiles FOR INSERT WITH CHECK (auth.jwt()->>'role' = 'ADMIN');
CREATE POLICY "Admins can update profiles" ON profiles FOR UPDATE USING (auth.jwt()->>'role' = 'ADMIN');

-- Appointments policies
CREATE POLICY "Users can view their own appointments" ON appointments FOR SELECT USING (auth.uid() = client_id OR auth.uid() = therapist_id);
CREATE POLICY "Admins can view all appointments" ON appointments FOR SELECT USING (auth.jwt()->>'role' = 'ADMIN');
CREATE POLICY "Users can create appointments" ON appointments FOR INSERT WITH CHECK (auth.uid() = client_id);
CREATE POLICY "Therapists can update appointments" ON appointments FOR UPDATE USING (auth.uid() = therapist_id);
CREATE POLICY "Admins can update appointments" ON appointments FOR UPDATE USING (auth.jwt()->>'role' = 'ADMIN');

-- Chat policies
CREATE POLICY "Users can view their own chat rooms" ON chat_rooms FOR SELECT USING (auth.uid() = client_id OR auth.uid() = therapist_id);
CREATE POLICY "Users can view messages in their rooms" ON chat_messages FOR SELECT USING (
  room_id IN (
    SELECT id FROM chat_rooms WHERE client_id = auth.uid() OR therapist_id = auth.uid()
  )
);
CREATE POLICY "Users can insert messages in their rooms" ON chat_messages FOR INSERT WITH CHECK (
  room_id IN (
    SELECT id FROM chat_rooms WHERE client_id = auth.uid() OR therapist_id = auth.uid()
  )
);

